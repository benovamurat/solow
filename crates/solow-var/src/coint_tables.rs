const NAN3: [f64; 3] = [f64::NAN, f64::NAN, f64::NAN];

/// Maximum-eigenvalue critical values for `n` common trends and deterministic
/// order `p` (reference `c_sja`).
pub fn c_sja(n: usize, p: i32) -> [f64; 3] {
    if !(-1..=1).contains(&p) || !(1..=12).contains(&n) {
        return NAN3;
    }
    match p {
        -1 => EJCP0[n - 1],
        0 => EJCP1[n - 1],
        _ => EJCP2[n - 1],
    }
}

/// Trace critical values for `n` common trends and deterministic order `p`
/// (reference `c_sjt`).
pub fn c_sjt(n: usize, p: i32) -> [f64; 3] {
    if !(-1..=1).contains(&p) || !(1..=12).contains(&n) {
        return NAN3;
    }
    match p {
        -1 => TJCP0[n - 1],
        0 => TJCP1[n - 1],
        _ => TJCP2[n - 1],
    }
}
